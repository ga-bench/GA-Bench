def _f(x):
    return "" if x is None or (isinstance(x, float) and x != x) else ("%.4f" % x)


def _series(d, key="mean"):
    return [d[yr][key] for yr in sorted(d.keys())]


def _make_figures(fig_dir, yrs, ov, adj, txt, vis, sem, pct, res, ocr):
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except Exception as e:
        print("[warn] matplotlib unavailable (%s); skipping figures." % e)
        return []
    made = []

    def save(fig, name):
        path = os.path.join(fig_dir, name)
        fig.tight_layout(); fig.savefig(path, dpi=150); plt.close(fig); made.append(name)

    # 1) overall raw vs adjusted with CI band
    fig, ax = plt.subplots(figsize=(8, 5))
    m = [ov[y]["mean"] for y in yrs]
    lo = [ov[y]["ci_lo"] for y in yrs]; hi = [ov[y]["ci_hi"] for y in yrs]
    ax.plot(yrs, m, "-o", label="raw mean")
    ax.fill_between(yrs, lo, hi, alpha=0.2, label="raw 95% CI")
    if adj:
        am = [adj[y]["mean"] for y in yrs if y in adj]
        ax.plot([y for y in yrs if y in adj], am, "-s", label="adjusted mean")
    ax.set_xlabel("publication year"); ax.set_ylabel("R_overall")
    ax.set_title("Overall readability by year (raw vs adjusted)")
    ax.legend(); ax.grid(alpha=0.3)
    save(fig, "overall_readability_by_year.png")

    # 2) components
    fig, ax = plt.subplots(figsize=(8, 5))
    for d, lab in ((txt, "R_text"), (vis, "R_visual"), (sem, "R_semantic")):
        ys = [y for y in yrs if y in d]
        ax.plot(ys, [d[y]["mean"] for y in ys], "-o", label=lab)
    ax.set_xlabel("publication year"); ax.set_ylabel("component score")
    ax.set_title("Readability components by year"); ax.legend(); ax.grid(alpha=0.3)
    save(fig, "components_by_year.png")

    # 3) % low / % high
    fig, ax = plt.subplots(figsize=(8, 5))
    ys = [y for y in yrs if y in pct]
    ax.plot(ys, [pct[y]["pct_low"] for y in ys], "-o", label="% low")
    ax.plot(ys, [pct[y]["pct_high"] for y in ys], "-o", label="% high")
    ax.set_xlabel("publication year"); ax.set_ylabel("percent of GAs")
    ax.set_title("Low / high readability share by year"); ax.legend(); ax.grid(alpha=0.3)
    save(fig, "pct_low_high_by_year.png")

    # 4) image quality twin axis
    fig, ax = plt.subplots(figsize=(8, 5))
    ys = [y for y in yrs if y in res]
    ax.plot(ys, [res[y]["mean"] for y in ys], "-o", color="tab:blue", label="resolution (MP)")
    ax.set_xlabel("publication year"); ax.set_ylabel("mean resolution (MP)", color="tab:blue")
    ax.tick_params(axis="y", labelcolor="tab:blue")
    ax2 = ax.twinx()
    yso = [y for y in yrs if y in ocr]
    ax2.plot(yso, [ocr[y]["mean"] for y in yso], "-s", color="tab:red", label="OCR confidence")
    ax2.set_ylabel("mean OCR confidence", color="tab:red")
    ax2.tick_params(axis="y", labelcolor="tab:red")
    ax.set_title("Image quality by year (artifact check)"); ax.grid(alpha=0.3)
    save(fig, "image_quality_by_year.png")

    return made


