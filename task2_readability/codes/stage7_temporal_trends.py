"""
Task 2 / Stage 7 — Temporal Trend Analysis (unweighted baseline).

Reads the Stage-5 readability scores and reports, per publication year:
  * mean / median / SD / 95% CI of R_overall and each component (R_text, R_visual,
    R_semantic)
  * % low- and % high-readability GAs
  * RAW yearly trends and CONFOUND-ADJUSTED yearly trends
  * image-quality variables by year (resolution, OCR confidence) plotted alongside
    readability, to check whether apparent trends track technical artifacts.

ADJUSTED trends are obtained by OLS residualization: R is regressed on the confounds
ONLY (domain + publisher + log resolution + OCR confidence, year EXCLUDED), then
adjusted_R = grand_mean + residual. The yearly mean of adjusted_R therefore removes
between-year differences attributable to confound composition. This is descriptive;
the inferential mixed model is Stage 9. Use ASSOCIATION language only.

Pure numpy (no pandas). matplotlib used only for figures (guarded import).

Outputs (under OUTPUT_DIR = output/stage7_temporal_trends/):
  tables/yearly_overall.csv
  tables/yearly_components.csv
  tables/yearly_image_quality.csv
  trends/*.png                 <- all figures here, as requested
  summary.json
  report.txt
"""

